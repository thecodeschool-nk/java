
public class Operator4Demo {

    public static void main(String[] args) {
        int a = 2, b = 3, c = 5;

        System.out.println((a & b));

        System.out.println(c & b);

        System.out.println(124 & 35);

        System.out.println(a | b);

        System.out.println(-2 << 2);
        System.out.println(-2 >> 2);


    }
}
